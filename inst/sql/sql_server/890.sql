(
  select o.* 
  FROM @cdm_database_schema.OBSERVATION o
JOIN #Codesets cs on (o.observation_concept_id = cs.concept_id and cs.codeset_id = 14)
) C


-- End Observation Criteria

UNION ALL
-- Begin Procedure Occurrence Criteria
